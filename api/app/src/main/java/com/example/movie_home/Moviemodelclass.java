package com.example.movie_home;

public class Moviemodelclass {

    String Detalhes;
    String Imagem;

    public Moviemodelclass( String Detalhes,String Imagem){
        this.Detalhes = Detalhes;
        this.Imagem = Imagem;
    }

    public String getDetalhes() {

        return this.Detalhes + Detalhes;
    }

    public String getImagem() {

        return this.Imagem + Imagem;
    }
}


